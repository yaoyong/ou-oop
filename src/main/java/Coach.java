public class Coach {
    private String name;
    private int age;
    private int champions;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getChampions() {
        return champions;
    }

    public void setChampions(int champions) {
        this.champions = champions;
    }
}
